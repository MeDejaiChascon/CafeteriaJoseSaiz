public class Cafe {
    private String Nombre;
    private int GranosDeCafe;
    private int MililitrosAgua;
    private String Tamanio;
    private String IngredienteAdicional;

    public Cafe(String nombre, int granosDeCafe, int mililitrosAgua, String tamanio, String ingredienteAdicional) {
        Nombre = nombre;
        GranosDeCafe = granosDeCafe;
        MililitrosAgua = mililitrosAgua;
        Tamanio = tamanio;
        IngredienteAdicional = ingredienteAdicional;
    }

}
